local function BuildExmlChangeTable_DeleteContrail(lst)
	local T = {}
	for i = 1, #lst do
		local section = {
			SPECIAL_KEY_WORDS	= {'Name', 'Contrail' .. lst[i]},
			REMOVE				= 'SECTION'
		}
		table.insert(T, section)
	end
	return T
end

