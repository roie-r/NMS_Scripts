local function randomRGB(T)
	for k, v in pairs(T) do
		if type(v) == 'table' then
			randomRGB(v)
		else
			if k == 'R' or k == 'G' or k == 'B' then
				T[k] = math.random()
			end
		end
	end
	return T
end

local function scramblePalettes(exml)
	return ToMxmlFile( randomRGB( ToLua(exml) ) )
end

