ProcessRawMxml = nil
function ProcessRawMxml(the_index) -- called by AMUMSS
	local T = {}
	for _,mbin in ipairs(source_mbins) do
		local norm_path	= NormalizePath(mbin, true)
		T[norm_path]	= scramblePalettes(table.concat(the_index.ModdedEXMLs[norm_path]))
	end
	return T
end

